    private void jTxtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTxtNombreKeyTyped
        // TODO add your handling code here:
         char c = evt.getKeyChar();
    String text = jTxtNombre.getText();

    // Verificar si el car√°cter es una letra (may√∫scula o min√∫scula) o un espacio
    if (!((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') || c == ' ')) {
        // Si no es una letra ni un espacio, consumir el evento para evitar que se ingrese
        evt.consume();
        return;
    }

    // Si el car√°cter es una letra y es el primer car√°cter o sigue a un espacio, convertir a may√∫scula
    if (c != ' ' && (text.isEmpty() || text.charAt(text.length() - 1) == ' ')) {
        c = Character.toUpperCase(c);
    } else if (c != ' ') {
        // Convertir las dem√°s letras a min√∫scula si es necesario
        c = Character.toLowerCase(c);
    }

    // Insertar el car√°cter modificado en el campo de texto
    jTxtNombre.setText(text + c);
    
    // Consumir el evento original para evitar la entrada duplicada
    evt.consume();
    }//GEN-LAST:event_jTxtNombreKeyTyped

