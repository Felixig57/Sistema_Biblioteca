    private void jTxtAnioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTxtAnioKeyTyped
        // TODO add your handling code here:
      char c = evt.getKeyChar();

    // Verificar si el car√°cter es un d√≠gito y si la longitud actual es menor que 4
    if (!Character.isDigit(c) || jTxtAnio.getText().length() >= 4) {
        // Si no es un d√≠gito o si ya hay 4 caracteres, consumir el evento
        evt.consume();
        return;
    }

    // Validar el a√±o en tiempo real
    String anio = jTxtAnio.getText() + c;
    if (anio.length() == 4) {
        try {
            int anioInt = Integer.parseInt(anio);
            // Obtener el a√±o actual
            int anioActual = java.time.Year.now().getValue();
            // Validar que el a√±o est√© dentro del rango permitido (1800 - a√±o actual)
            if (anioInt < 1800 || anioInt > anioActual) {
                // Si el a√±o no es v√°lido, mostrar un cuadro de di√°logo de error y borrar el campo
                JOptionPane.showMessageDialog(null, "A√±o inv√°lido", "Error", JOptionPane.ERROR_MESSAGE);
                jTxtAnio.setText("");
                evt.consume();
            }
        } catch (NumberFormatException e) {
            // Si ocurre una excepci√≥n al convertir a entero, el a√±o no es v√°lido
            JOptionPane.showMessageDialog(null, "A√±o inv√°lido", "Error", JOptionPane.ERROR_MESSAGE);
            jTxtAnio.setText("");
            evt.consume();
        }
    }
    }//GEN-LAST:event_jTxtAnioKeyTyped

